def _read_env() -> dict[str, str]:
    path = ENV_FILE if ENV_FILE.exists() else (ENV_STAGING if ENV_STAGING.exists() else None)
    if not path:
        return {}
    result: dict[str, str] = {}
    for line in path.read_text().splitlines():
        line = line.strip()
        if line and not line.startswith("#") and "=" in line:
            k, _, v = line.partition("=")
            result[k.strip()] = v.strip()
    return result


def _write_env(fields: dict[str, str]) -> None:
    HIS_DIR.mkdir(parents=True, exist_ok=True)
    # Write to staging path — _ensure_repo moves it into REPO_DIR after clone
    ENV_STAGING.write_text(ENV_TEMPLATE.format(**fields))


# ── Git helpers ────────────────────────────────────────────────────────────────

def _git_url_with_token(url: str, token: str) -> str:
    """Inject token into https URL: https://TOKEN@github.com/..."""
    if token and url.startswith("https://"):
        return url.replace("https://", f"https://{token}@", 1)
    return url


def _validate_repo(url: str, token: str) -> tuple[bool, str]:
    """Try a shallow ls-remote to check access. Returns (ok, message)."""
    auth_url = _git_url_with_token(url, token)
    result = subprocess.run(
        ["git", "ls-remote", "--heads", auth_url],
        capture_output=True, text=True, timeout=15,
    )
    if result.returncode == 0:
        return True, "Repository accessible."
    err = result.stderr.strip().splitlines()[-1] if result.stderr.strip() else "unknown error"
    return False, err


def _ensure_repo(url: str, token: str, log_fn) -> bool:
    auth_url = _git_url_with_token(url, token)
    if not COMPOSE_FILE.exists():
        # Remove empty dir if pre-created
        if REPO_DIR.exists() and not any(REPO_DIR.iterdir()):
            REPO_DIR.rmdir()
        log_fn("→ Cloning HIS repository…")
        r = subprocess.run(
            ["git", "clone", "--depth=1", auth_url, str(REPO_DIR)],
            capture_output=True, text=True,
        )
        if r.returncode != 0:
            log_fn(f"✗ git clone failed: {r.stderr.strip().splitlines()[-1]}")
            return False
        log_fn("✓ Repository cloned.")
    else:
        log_fn("→ Pulling latest…")
        subprocess.run(
            ["git", "-C", str(REPO_DIR), "remote", "set-url", "origin", auth_url],
            capture_output=True,
        )
        subprocess.run(
            ["git", "-C", str(REPO_DIR), "pull", "--ff-only"],
            capture_output=True,
        )
        log_fn("✓ Up to date.")
    # Move staging .env into STACK_DIR now that repo is cloned
    if ENV_STAGING.exists():
        STACK_DIR.mkdir(parents=True, exist_ok=True)
        ENV_STAGING.rename(ENV_FILE)
    return True


def _run_compose(log_fn) -> bool:
    ansi = re.compile(r"\x1b\[[0-9;]*m")
    proc = subprocess.Popen(
        ["docker", "compose",
         "-f", str(COMPOSE_FILE), "-f", str(COMPOSE_ADDON),
         "--env-file", str(ENV_FILE),
         "up", "-d", "--remove-orphans"],
        stdout=subprocess.PIPE, stderr=subprocess.STDOUT,
        text=True, cwd=str(STACK_DIR),
    )
    for line in proc.stdout:
        clean = ansi.sub("", line).rstrip()
        if clean:
            log_fn(clean)
    proc.wait()
    if proc.returncode != 0:
        log_fn(f"✗ docker compose exited with code {proc.returncode}")
        return False

    log_fn("→ Running database migrations…")
    mig = subprocess.run([
        "docker", "compose",
        "-f", str(COMPOSE_FILE), "--env-file", str(ENV_FILE),
        "run", "--rm",
        "-e", "PYTHONPATH=/app:/app/shared_lib:/app/knowledge_lib",
        "--entrypoint", "", "gateway",
        "sh", "-c", "cd /app/knowledge_lib && alembic upgrade head",
    ], capture_output=True, text=True, cwd=str(STACK_DIR))
    for line in (mig.stdout + mig.stderr).splitlines():
        if line.strip():
            log_fn(line)
    log_fn("✓ Migrations applied." if mig.returncode == 0
           else "⚠ Migrations failed — stack running but DB may be incomplete.")

    log_fn("→ Connecting add-on to HIS network…")
    hostname = os.environ.get("HOSTNAME", "")
    if hostname:
        r = subprocess.run(["docker", "network", "connect", "his_his_net", hostname],
                           capture_output=True, text=True)
        log_fn("✓ Joined his_his_net." if r.returncode == 0
               else f"  (network: {r.stderr.strip() or 'already connected'})")
    return True


