"""
HIS Add-on Orchestrator
=======================
Public orchestrator — no HIS source code here, no hardcoded repo URL.

The wizard collects:
  1. HIS repo URL + optional GitHub token  (validated before proceeding)
  2. LLM provider + API key
  3. Telegram bot token + user ID           (optional)
  4. Public URL                             (optional, needed for Telegram)

HA URL and access token are auto-provisioned from the Supervisor API.

Routes:
  GET  /                       → wizard HTML
  GET  /prefill                → pre-filled values (Supervisor + existing config)
  POST /validate-repo          → test git clone access
  POST /configure              → write /share/his/his/.env
  GET  /deploy                 → SSE: git clone → compose up → migrations
  GET  /providers/{p}/models   → live model list from provider API
  POST /validate-domain        → test public URL reachability
  GET  /uninstall              → SSE: compose down -v → rm -rf /share/his
  GET  /health                 → {"status":"ok"}
"""

