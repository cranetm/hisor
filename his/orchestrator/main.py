# ── Model list ─────────────────────────────────────────────────────────────────

_MODEL_CACHE: dict[tuple, tuple[float, list]] = {}
_MODEL_TTL = 300


def _fetch_models(provider: str, api_key: str | None) -> list[dict]:
    def _get(url: str, headers: dict) -> dict:
        req = urllib.request.Request(url, headers=headers)
        ctx = ssl.create_default_context() if url.startswith("https") else None
        with urllib.request.urlopen(req, timeout=12, **({"context": ctx} if ctx else {})) as r:
            return json.loads(r.read())

    def _pretty(mid: str) -> str:
        return mid.split("/")[-1].split(":")[0].replace("-", " ").replace("_", " ").title()

    if provider == "groq":
        data = _get("https://api.groq.com/openai/v1/models",
                    {"Authorization": f"Bearer {api_key or ''}", "User-Agent": "HIS/1.0"})
        rec = {"llama-3.3-70b-versatile", "meta-llama/llama-4-scout-17b-16e-instruct", "llama-3.1-8b-instant"}
        return sorted([
            {"id": m["id"], "name": _pretty(m["id"]), "context_length": m.get("context_window", 0),
             "supports_tools": True, "recommended": m["id"] in rec, "tags": ["chat"]}
            for m in data.get("data", [])
            if not any(k in m["id"].lower() for k in ("whisper", "guard", "tts"))
        ], key=lambda x: (not x["recommended"], -(x["context_length"] or 0)))

    if provider == "openrouter":
        data = _get("https://openrouter.ai/api/v1/models",
                    {"Authorization": f"Bearer {api_key or ''}", "HTTP-Referer": "https://his.local",
                     "X-Title": "HIS", "User-Agent": "HIS/1.0"})
        return sorted([
            {"id": m["id"], "name": m.get("name") or _pretty(m["id"]),
             "context_length": m.get("context_length", 0), "supports_tools": True,
             "recommended": any(k in m["id"].lower() for k in ("deepseek", "gemma", "qwen")),
             "tags": ["free"]}
            for m in data.get("data", [])
            if str(m.get("pricing", {}).get("prompt", "1")) in ("0", "0.0", "0.00")
        ], key=lambda x: (not x["recommended"], -(x["context_length"] or 0)))

    if provider == "ollama":
        base = (api_key or "http://ollama:11434").rstrip("/")
        data = _get(f"{base}/api/tags", {"User-Agent": "HIS/1.0"})
        return [
            {"id": m.get("name", ""), "name": _pretty(m.get("name", "")),
             "context_length": 0, "supports_tools": True,
             "recommended": any(k in m.get("name", "").lower() for k in ("llama3", "qwen", "mistral")),
             "tags": ["local"]}
            for m in data.get("models", [])
            if "embed" not in m.get("name", "").lower()
        ]

    if provider == "gemini":
        data = _get(
            f"https://generativelanguage.googleapis.com/v1beta/models?key={api_key or ''}",
            {"User-Agent": "HIS/1.0"},
        )
        rec = {"gemini-2.0-flash", "gemini-2.5-flash", "gemini-3.5-flash"}
        return sorted([
            {"id": m["name"].removeprefix("models/"), "name": m.get("displayName", m["name"]),
             "context_length": m.get("inputTokenLimit", 0), "supports_tools": True,
             "recommended": m["name"].removeprefix("models/") in rec, "tags": ["chat"]}
            for m in data.get("models", [])
            if "embedding" not in m["name"].lower()
            and "generateContent" in m.get("supportedGenerationMethods", [])
        ], key=lambda x: (not x["recommended"], -(x["context_length"] or 0)))

    return []


